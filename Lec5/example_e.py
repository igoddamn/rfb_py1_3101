# Цикл while

i = 0                       # переменной цикла
while i <= 10:              # условие цикла (True, False - тело выполнено не будет)
    print(f"This is {i}")   # тело цикла
    i = i + 1              # инкремент\декремент переменной цикла


# Итерация - разовое выполенение тела цикла
# Процесс работы цикла - итерирование
print("Out while loop")